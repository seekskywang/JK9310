void SPI_Touch_WriteByte(uint8_t dat)//触摸屏的SPI写数据
{
	u8 count=0;   
	for(count=0;count<8;count++)  
	{ 	  
		if(dat&0x80)_SPI_TOUCH_DOUTH;  
		else _SPI_TOUCH_DOUTL;   
		dat<<=1;    
		_SPI_TOUCH_CLKL; 
		delay_us(1);
		_SPI_TOUCH_CLKH;		//上升沿有效	        
	}	

}
